package com.tutorial;

public class LoginBean {

	private String name;
	private String password;
	
	public String getName()
	{
		return name;
	}
	public void setName(String value)
	{
		name = value;
	}
	public String getPassword()
	{
		return password;
	}
	public void setPassword(final String value)
	{
		password = value;
	}
}
